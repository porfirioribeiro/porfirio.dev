<h1>About this website</h1>

<p>
  For a long time I've been willing to make a website and a blog for myself. I
  ended up trying every new and shine technology that appear to make the
  website.
  <br />
  But then I got lost on engineering the site so much that I ended up quitting it,
  or another shining piece of tech appears.
</p>
<p>
  I think I first make the website in PHP. I was learning it, and liked all the
  fancy stuff you could do. So I created all this big MVC system, with database
  layer, controllers and fancy stuff. But it never saw the light.
  <br />
  Later I started learning more about JavaScript and heard about Ghost. I remember
  making some stuff on it, but haven't got too far.
</p>
<p>
  I started working as a frontend developer with React, so when I heard about
  Gatsby I was all in. I made the company website in it, so I also decided to do
  my website and blog on it. Had the structure but then got with CMS and stuff I
  could use to publish to my empty blog, and ended up loosing attention on it.
</p>
<p>
  Mostly because I found Svelte, and I fall in love with it right a way. Even
  tho I barely used Svelte professionally, it soon became one of my favorite
  frontend frameworks.
  <br />
  So I started a new site with Sapper, the Gatsby/Next framework like of Svelte.
  It was very nice to work, apart from some quirks, felt allot better than working
  with Gatsby and React. But then...
</p>
<p>
  Last year Rich Harris announced the born of a new framework for Svelte, that
  would be a better Sapper, with better API's and integrations. I promptly
  started to build a new website and a blog with it for exploring it's
  capacities, even being a beta technology.
</p>
<p>
  At some point I wanted to have a site with some content about me, so I decided
  to stop over-engineering stuff and build a quick website, just with some
  information about me, who I am and what I did.
  <br />
  Pretty simple, no fancy animations and stuff, just focused on content first. Later
  I hope to make it more pretty and responsive, add a blog and maybe over-engineer
  some things.
</p>
